package wordprocessor;

/**
 * Enum for specifying changes to a Fragment's style.
 *
 * @author Markus Iser, Florian Merz
 * @version 1.0
 */
public enum StyleChange {
    /** set the text bold */
    SET_BOLD,
    /** set the text non-bold */
    UNSET_BOLD,
    /** set the text italic */
    SET_ITALIC,
    /** set the text non-italic */
    UNSET_ITALIC
}
